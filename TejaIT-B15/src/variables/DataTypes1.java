package variables;

public class DataTypes1 {

	public static void main(String[] args) {
		//primitive - 8
		
		//number data type - 4
		byte id=120;
		short num=12123;
		int value=74165;
		long mobile=7416501067l;
		
		//decimal data type - 2
		double balance=1475.00;
		float miles=75.5f;
		
		
		//char data type - 1
		char grade = 'a';
		
		//boolean data type - 1
		boolean areyouindian = true;
		
		
		System.out.println("id value::"+id);
		System.out.println("short number::"+num);
		System.out.println("int value::"+value);
		System.out.println("long mobile::"+mobile);
		System.out.println("double balance::"+balance);
		System.out.println("float miles::"+miles);
		System.out.println("chat grade::"+grade);
		System.out.println("boolean areyouindian::"+areyouindian);
		
		
		
}
}