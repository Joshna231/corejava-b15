package variables;

public class DataTypes2 {
	public static void main(String[] args) {
		//objective data types-classes
		//datatype & classes
		Byte id=123;
		Short num=12123;
		Integer value=123456;
		Long mobile=6304368269l;
		Float miles=4.25f;
		Double amt=14.65;
		Character grade='b';
		Boolean areyouIndian=true;
		String name="joshna";
		
		System.out.println("Byte id::"+id);
		System.out.println("Short num::"+num);
		System.out.println("Integer value::"+value);
		System.out.println("Long mobile::"+mobile);
		System.out.println("Float miles::"+miles);
		System.out.println("Double amt::"+amt);
		System.out.println("Character grade::"+grade);
		System.out.println("Boolean areyouIndian::"+areyouIndian);
		System.out.println("String name::"+name);
	}
	}